// src/lib/experiments/types.ts
// Shared types for the A/B Testing & Experiment Engine.

import type { BusinessKey } from '@/lib/businesses'

// ── Experiment Types ───────────────────────────────────────────────────────────

export const EXPERIMENT_TYPES = [
  'social_copy',
  'social_media',
  'social_timing',
  'social_platform',
  'cta_variation',
  'subject_line',
  'landing_page',
  'offer_test',
] as const

export type ExperimentType = (typeof EXPERIMENT_TYPES)[number]

// ── Experiment Statuses ────────────────────────────────────────────────────────

export const EXPERIMENT_STATUSES = [
  'draft',
  'active',
  'paused',
  'completed',
  'cancelled',
] as const

export type ExperimentStatus = (typeof EXPERIMENT_STATUSES)[number]

// ── Primary Metrics ────────────────────────────────────────────────────────────

export const PRIMARY_METRICS = [
  'engagement',
  'clicks',
  'conversions',
  'reach',
] as const

export type PrimaryMetric = (typeof PRIMARY_METRICS)[number]

// ── Result Sources ─────────────────────────────────────────────────────────────

export const RESULT_SOURCES = [
  'manual',
  'api_sync',
  'webhook',
] as const

export type ResultSource = (typeof RESULT_SOURCES)[number]

// ── Generated By ───────────────────────────────────────────────────────────────

export const GENERATED_BY_OPTIONS = [
  'synthex_ai',
  'manual',
] as const

export type GeneratedBy = (typeof GENERATED_BY_OPTIONS)[number]

// ── Database Row Types ─────────────────────────────────────────────────────────

export interface Experiment {
  id: string
  founderId: string
  businessKey: BusinessKey
  title: string
  hypothesis: string
  experimentType: ExperimentType
  status: ExperimentStatus
  generatedBy: GeneratedBy | null
  aiRationale: string | null
  metricPrimary: PrimaryMetric
  metricSecondary: string | null
  sampleSizeTarget: number | null
  confidenceLevel: number
  startedAt: string | null
  endedAt: string | null
  winnerVariantId: string | null
  conclusion: string | null
  approvalQueueId: string | null
  createdAt: string
  updatedAt: string
}

export interface ExperimentVariant {
  id: string
  experimentId: string
  founderId: string
  variantKey: string
  label: string
  description: string | null
  content: string | null
  mediaUrls: string[]
  ctaText: string | null
  scheduledTime: string | null
  platforms: string[]
  isControl: boolean
  weight: number
  createdAt: string
  updatedAt: string
}

export interface ExperimentResult {
  id: string
  variantId: string
  experimentId: string
  founderId: string
  periodDate: string
  impressions: number
  reach: number
  clicks: number
  likes: number
  comments: number
  shares: number
  saves: number
  conversions: number
  conversionValueCents: number
  platformData: Record<string, unknown>
  source: ResultSource
  createdAt: string
}

// ── UI Summary Types ───────────────────────────────────────────────────────────

export interface VariantAggregatedTotals {
  totalImpressions: number
  totalReach: number
  totalClicks: number
  totalLikes: number
  totalComments: number
  totalShares: number
  totalSaves: number
  totalConversions: number
  totalConversionValueCents: number
  engagementRate: number
  clickThroughRate: number
  conversionRate: number
}

export interface VariantWithTotals extends ExperimentVariant {
  totals: VariantAggregatedTotals
}

export interface SignificanceData {
  zScore: number
  pValue: number
  isSignificant: boolean
  controlRate: number
  treatmentRate: number
  lift: number
}

export interface ExperimentSummary {
  experiment: Experiment
  variants: VariantWithTotals[]
  significance: Record<string, SignificanceData | null>
  winnerVariant: VariantWithTotals | null
  totalSampleSize: number
  daysRunning: number
}

// ── API Request/Response Types ─────────────────────────────────────────────────

export interface GenerateExperimentRequest {
  businessKey: BusinessKey
  experimentType?: ExperimentType
  focusArea?: string
}

export interface GeneratedVariantConfig {
  variantKey: string
  label: string
  description: string
  content: string
  ctaText: string | null
  scheduledTime: string | null
  platforms: string[]
  isControl: boolean
  weight: number
}

export interface GenerateExperimentResponse {
  title: string
  hypothesis: string
  experimentType: ExperimentType
  metricPrimary: PrimaryMetric
  metricSecondary: string | null
  sampleSizeTarget: number | null
  confidenceLevel: number
  aiRationale: string
  variants: GeneratedVariantConfig[]
}

export interface ExperimentListResponse {
  experiments: Experiment[]
  total: number
  page: number
  pageSize: number
}

export interface ExperimentDetailResponse {
  experiment: Experiment
  variants: ExperimentVariant[]
  results: ExperimentResult[]
  summary: ExperimentSummary
}
